public class Task1 {


    /*
     Task1. Write a program that prints all numbers between -100 and 100. Then print them backwards.
     */

    public static void main(String[] args) {
        for (int i =-100; i <= 100; i ++){
            System.out.println(i);
        } for (int i =100; i>=-100; i--){
            System.out.println(i);
        }
    }
}
